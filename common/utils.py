def format_file_size(file_size):
    for count in ['Bytes', 'KB', 'MB', 'GB']:
        if -1024.0 < file_size < 1024.0:
            return "%3.1f%s" % (file_size, count)
        file_size /= 1024.0
    return "%3.1f%s" % (file_size, 'TB')


def md5(data, block_size=65536):
    # 创建md5对象
    m = hashlib.md5()
    # 对django中的文件对象进行迭代
    for item in iter(partial(data.read, block_size), b''):
        # 把迭代后的bytes加入到md5对象中
        m.update(item)
    str_md5 = m.hexdigest()
    return str_md5


